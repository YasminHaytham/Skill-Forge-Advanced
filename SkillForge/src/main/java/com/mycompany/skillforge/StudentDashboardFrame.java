    private void UnenrollBtnActionPerformed(java.awt.event.ActionEvent evt) {
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to Unenroll From Course?",
                "Confirm Unenroll",
                JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        } else {
            int selected = EnrolledCoursesList.getSelectedIndex();
            if (selected != -1) {
                Course courseToUnenroll = enrolledCourses.get(selected);
                courseToUnenroll.unenrollStudent(Student);
                enrolledCourses = Student.getEnrolledCourseObjects();
                LoadEnrolledCoursesTolist();
                LoadAvailableCoursesToList();
                LessonList.setVisible(false);
                DefaultListModel<String> emptyModel = new DefaultListModel<>();
                LessonList.setModel(emptyModel);
                LessonFrameBtn.setVisible(false);
                JOptionPane.showMessageDialog(this, "Successfully unenrolled from course!", "Success", JOptionPane.INFORMATION_MESSAGE);
                

            } else {
                JOptionPane.showMessageDialog(this, "Please select a course to unenroll.");
            }
        }
    }                                           

    private void EnrollBtnActionPerformed(java.awt.event.ActionEvent evt) {
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to Enroll in Course?",
                "Confirm Enroll",
                JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        } else {
            int selected = AvailableCoursesList.getSelectedIndex();
            if (selected != -1) {
                Course courseToEnroll = availableCourses.get(selected);
                courseToEnroll.enrollStudent(Student);
                enrolledCourses = Student.getEnrolledCourseObjects();
                LoadEnrolledCoursesTolist();
                LoadAvailableCoursesToList();

                JOptionPane.showMessageDialog(this, "Successfully enrolled in course!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Please select a course to enroll.");
            }
        }
    }                                         

