    private void ViewLessonsBtnActionPerformed(java.awt.event.ActionEvent evt) {
       // addLessonProgress(String courseId, String LessonId);
        LessonList.setVisible(true);
        int selected = EnrolledCoursesList.getSelectedIndex();
        if (selected != -1) {
            Course selectedCourse = Student.getEnrolledCourseObjects().get(selected);
            List<Lesson> lessons = selectedCourse.getLessons();
            DefaultListModel<String> model = new DefaultListModel<>();
            CompletedLessons = Student.getCompletedLessons();
            for (Lesson lesson : lessons) {
                boolean isCompleted = false;
                for (Lesson completed : CompletedLessons) {
                    if (completed.getLessonId().equals(lesson.getLessonId())) {
                        isCompleted = true;
                        break;
                    }
                }
                String status = isCompleted ? "✓ " : "○ ";
                model.addElement(status + lesson.getTitle());
            }

            LessonList.setModel(model);
            LessonList.setVisible(true);
            LessonFrameBtn.setVisible(true);

              LessonList.revalidate();
        LessonList.repaint();
        jScrollPane3.revalidate();
        jScrollPane3.repaint();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a course to view lessons.");
        }
    }

    private void LoadEnrolledCoursesTolist() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Course course : enrolledCourses) {
            model.addElement(course.getTitle());
        }
        EnrolledCoursesList.setModel(model);
          EnrolledCoursesList.revalidate();
    EnrolledCoursesList.repaint();
    jScrollPane2.revalidate();
    jScrollPane2.repaint();
    }

    private void LoadAvailableCoursesToList() {
        DefaultListModel<String> model = new DefaultListModel<>();

        for (Course course : availableCourses) {
            if(course.isApproved()){
            if (!course.isStudentEnrolled(Student)) {
                model.addElement(course.getTitle());
            }
        }
    }
        AvailableCoursesList.setModel(model);
          AvailableCoursesList.revalidate();
    AvailableCoursesList.repaint();
    jScrollPane2.revalidate();
    jScrollPane2.repaint();
    }

